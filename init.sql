-- ===========================================================================
-- LUỒNG 1: TỔ CHỨC, NHÂN SỰ & PHÂN QUYỀN
-- ===========================================================================

CREATE TABLE VAI_TRO (
    MAVAITRO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TENVAITRO VARCHAR2(100) NOT NULL,
    QUYENHAN CLOB CHECK (QUYENHAN IS JSON) NOT NULL
);

CREATE TABLE NHANVIEN (
    MANHANVIEN NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MAVAITRO NUMBER REFERENCES VAI_TRO(MAVAITRO), 
    HOTEN VARCHAR2(255) NOT NULL,
    SDT VARCHAR2(20) NOT NULL,
    EMAIL VARCHAR2(100) NOT NULL,
    NGAYVAOLAM DATE NOT NULL,
    TRANGTHAI VARCHAR2(50) NOT NULL, 
    USERNAME VARCHAR2(100) UNIQUE,
    PASSWORDHASH VARCHAR2(255)
);

CREATE TABLE HO_SO_LUONG (
    MANHANVIEN NUMBER PRIMARY KEY REFERENCES NHANVIEN(MANHANVIEN) ON DELETE CASCADE,
    MUCLUONG NUMBER DEFAULT 0 NOT NULL, 
    GIAMTRUBANTHAN NUMBER DEFAULT 15500000 NOT NULL, 
    SONGUOIPHUTHUOC NUMBER DEFAULT 0 NOT NULL,
    TIENGIAMNPT NUMBER DEFAULT 0 NOT NULL, 
    NGAYCAPNHAT DATE DEFAULT SYSDATE NOT NULL,
    BHTUYCHINH NUMBER DEFAULT -1 NOT NULL,
    THUETUYCHINH NUMBER DEFAULT -1 NOT NULL
);

CREATE TABLE PHIEU_LUONG (
    MAPHIEU NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MANHANVIEN NUMBER REFERENCES NHANVIEN(MANHANVIEN),
    THANGNAM VARCHAR2(7) NOT NULL, 
    LUONG NUMBER NOT NULL, 
    TONGBAOHIEMNV NUMBER DEFAULT 0 NOT NULL,
    TONGTHUETNCN NUMBER DEFAULT 0 NOT NULL,
    THUCLINH NUMBER NOT NULL, 
    TRANGTHAI VARCHAR2(50) DEFAULT 'Chờ duyệt' NOT NULL
);

CREATE TABLE CHI_TIET_BAO_HIEM (
    MAPHIEU NUMBER PRIMARY KEY REFERENCES PHIEU_LUONG(MAPHIEU) ON DELETE CASCADE,
    BHXH_NV NUMBER DEFAULT 0, BHYT_NV NUMBER DEFAULT 0, BHTN_NV NUMBER DEFAULT 0,
    BHXH_DN NUMBER DEFAULT 0, BHYT_DN NUMBER DEFAULT 0, BHTN_DN NUMBER DEFAULT 0,
    TNLDBNN_DN NUMBER DEFAULT 0 
);

CREATE TABLE CHI_TIET_THUE_TNCN (
    MACHITIET NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MAPHIEU NUMBER REFERENCES PHIEU_LUONG(MAPHIEU) ON DELETE CASCADE,
    BACTHUE NUMBER NOT NULL,
    THUNHAPCHIUTHUE NUMBER NOT NULL,
    TIENTHUE NUMBER NOT NULL
);

CREATE TABLE CHAM_CONG (
    MACHAMCONG NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MANHANVIEN NUMBER REFERENCES NHANVIEN(MANHANVIEN),
    NGAY DATE DEFAULT SYSDATE NOT NULL,
    GIOVAO TIMESTAMP WITH TIME ZONE,
    GIORA TIMESTAMP WITH TIME ZONE,
    SOGIOLAM NUMBER(6,2) DEFAULT 0,
    TANGCA NUMBER(6,2) DEFAULT 0,
    TRANGTHAI VARCHAR2(50),
    GHICHU VARCHAR2(255)
);

CREATE TABLE QUAN_LY_PHEP (
    IDPHEP NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MANHANVIEN NUMBER REFERENCES NHANVIEN(MANHANVIEN),
    NAM NUMBER NOT NULL,
    TONGPHEP NUMBER NOT NULL,
    DADUNG NUMBER DEFAULT 0 NOT NULL,
    CONLAI NUMBER NOT NULL
);

CREATE TABLE DON_XIN_NGHI_PHEP (
    MADON NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MANHANVIEN NUMBER REFERENCES NHANVIEN(MANHANVIEN),
    LOAINGHI VARCHAR2(100) NOT NULL,
    TUNGAY DATE NOT NULL,
    DENNGAY DATE NOT NULL,
    SONGAY NUMBER NOT NULL,
    LYDO VARCHAR2(255),
    TRANGTHAI VARCHAR2(50) DEFAULT 'Chờ duyệt' NOT NULL,
    NGUOIDUYET NUMBER REFERENCES NHANVIEN(MANHANVIEN)
);

-- ===========================================================================
-- LUỒNG 2: ĐỐI TÁC & THÚ CƯNG
-- ===========================================================================

CREATE TABLE DOI_TAC (
    MADOITAC NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TENDOITAC VARCHAR2(255) NOT NULL,
    SODIENTHOAI VARCHAR2(20),
    DIACHI VARCHAR2(255),
    EMAIL VARCHAR2(100),
    LOAIDOITAC VARCHAR2(50),
    TRANGTHAI NUMBER(1) DEFAULT 1
);

CREATE TABLE KHACH_HANG (
    MADOITAC NUMBER PRIMARY KEY REFERENCES DOI_TAC(MADOITAC) ON DELETE CASCADE,
    DIEMTICHLUY NUMBER DEFAULT 0 NOT NULL,
    NGAYTHAMGIA DATE DEFAULT SYSDATE NOT NULL,
    LOAIKHACHHANG VARCHAR2(50) DEFAULT 'Đồng' NOT NULL,
    TRANGTHAI NUMBER(1) DEFAULT 1
);

CREATE TABLE NHA_CUNG_CAP (
    MADOITAC NUMBER PRIMARY KEY REFERENCES DOI_TAC(MADOITAC) ON DELETE CASCADE,
    MASOTHUE VARCHAR2(50) NOT NULL,
    DIEUKHOANTHANHTOAN VARCHAR2(255),
    GHICHU VARCHAR2(255)
);

CREATE TABLE HO_SO_THU_CUNG (
    MATHUCUNG NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MADOITAC NUMBER REFERENCES KHACH_HANG(MADOITAC),
    TENTHUCUNG VARCHAR2(100) NOT NULL,
    LOAITHUCUNG VARCHAR2(50),
    GIOITINH NUMBER(1) CHECK (GIOITINH IN (0, 1)) 
);

-- ===========================================================================
-- LUỒNG 3: SẢN PHẨM & DỊCH VỤ
-- ===========================================================================

CREATE TABLE SAN_PHAM (
    MASANPHAM NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TENSANPHAM VARCHAR2(255) NOT NULL,
    DONVITINH VARCHAR2(50) NOT NULL,
    GIANIEMYET NUMBER NOT NULL,
    THUE NUMBER NOT NULL,
    MAVACH VARCHAR2(100),
    COTHEMUA NUMBER(1) CHECK (COTHEMUA IN (0, 1)), 
    COTHEBAN NUMBER(1) CHECK (COTHEBAN IN (0, 1)), 
    POS NUMBER(1) CHECK (POS IN (0, 1)),
    THUONGHIEU VARCHAR2(100),
    XUATXU VARCHAR2(100),
    PHUHOP VARCHAR2(100),
    THANHPHAN CLOB,
    HUONGDAN CLOB,
    MOTA CLOB,
    HINHANH VARCHAR2(500)
);

CREATE TABLE DICH_VU (
    MADICHVU NUMBER GENERATED ALWAYS AS IDENTITY,
    TENDICHVU NVARCHAR2(255),
    GIA NUMBER,
    COTHEBAN NUMBER(1) DEFAULT 1 CHECK (COTHEBAN IN (0, 1)),
    PRIMARY KEY(MADICHVU)
);

-- ===========================================================================
-- LUỒNG 4: NHẬP HÀNG & TỒN KHO TRỰC TIẾP
-- ===========================================================================

CREATE TABLE HOA_DON_NHAP_HANG (
    MAHOADONNHAP NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MADOITAC NUMBER REFERENCES NHA_CUNG_CAP(MADOITAC),
    MANHANVIEN NUMBER REFERENCES NHANVIEN(MANHANVIEN),
    SOHOADON_VAT VARCHAR2(100),
    NGAYLAP DATE DEFAULT SYSDATE NOT NULL,
    TONGTIEN NUMBER NOT NULL,
    PHUONGTHUCTHANHTOAN VARCHAR2(50),
    TRANGTHAI_THANHTOAN VARCHAR2(50),
    GHICHU VARCHAR2(255)
);

CREATE TABLE CHI_TIET_HOA_DON_NHAP (
    MACHITIET NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MAHOADONNHAP NUMBER REFERENCES HOA_DON_NHAP_HANG(MAHOADONNHAP) ON DELETE CASCADE,
    MASANPHAM NUMBER REFERENCES SAN_PHAM(MASANPHAM),
    SOLUONG NUMBER NOT NULL,
    SOLUONGDANHAN NUMBER DEFAULT 0 NOT NULL,
    DONGIA NUMBER NOT NULL,
    THANHTIEN NUMBER NOT NULL
);

-- Cập nhật: Khóa chính chỉ còn MASANPHAM
CREATE TABLE TON_KHO (
    MASANPHAM NUMBER PRIMARY KEY REFERENCES SAN_PHAM(MASANPHAM),
    SOLUONGTON NUMBER DEFAULT 0 NOT NULL,
    NGAYCAPNHAT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL
);

CREATE TABLE KIEM_KE (
    MAKIEMKE NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NGAYKIEMKE DATE DEFAULT SYSDATE NOT NULL,
    NGUOIKIEMKE NUMBER REFERENCES NHANVIEN(MANHANVIEN),
    TRANGTHAI VARCHAR2(50) 
);

CREATE TABLE CHI_TIET_KIEM_KE (
    MACHITIET NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MAKIEMKE NUMBER REFERENCES KIEM_KE(MAKIEMKE) ON DELETE CASCADE,
    MASANPHAM NUMBER REFERENCES SAN_PHAM(MASANPHAM),
    SLHETHONG NUMBER NOT NULL,
    SLTHUCTE NUMBER NOT NULL,
    SLLECH NUMBER NOT NULL,
    LYDOLECH VARCHAR2(255)
);

-- ===========================================================================
-- LUỒNG 5: BÁN HÀNG & LỊCH HẸN SPA/PET
-- ===========================================================================

CREATE TABLE DON_HANG (
    MADONHANG NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MADOITAC NUMBER REFERENCES KHACH_HANG(MADOITAC),
    MANHANVIEN NUMBER REFERENCES NHANVIEN(MANHANVIEN),
    NGAYTAO TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    TONGTIEN NUMBER DEFAULT 0 NOT NULL,
    TRANGTHAI VARCHAR2(50) DEFAULT 'ChoXacNhan' NOT NULL,
    GHICHU VARCHAR2(255),
    DIEMSUDUNG NUMBER DEFAULT 0,
    THOIGIANTHANHTOAN TIMESTAMP
);

CREATE TABLE CHI_TIET_DON_HANG (
    MACHITIET NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MADONHANG NUMBER REFERENCES DON_HANG(MADONHANG) ON DELETE CASCADE,
    MASANPHAM NUMBER REFERENCES SAN_PHAM(MASANPHAM),
    MADICHVU NUMBER REFERENCES DICH_VU(MADICHVU), 
    SOLUONG NUMBER NOT NULL,
    DONGIA NUMBER NOT NULL,
    THANHTIEN NUMBER NOT NULL
);

CREATE TABLE LICH_HEN (
    MALICHHEN NUMBER GENERATED ALWAYS AS IDENTITY,
    MAKH NUMBER REFERENCES KHACH_HANG(MADOITAC),
    MATHUCUNG NUMBER REFERENCES HO_SO_THU_CUNG(MATHUCUNG),
    THOIGIANHEN TIMESTAMP,
    TRANGTHAI NVARCHAR2(255) DEFAULT 'Đợi check-in', 
    MANV NUMBER REFERENCES NHANVIEN(MANHANVIEN),
    PRIMARY KEY(MALICHHEN, MAKH) 
);

CREATE TABLE CHI_TIET_LICH_HEN (
    MALICHHEN NUMBER,
    MAKH NUMBER, 
    MADICHVU NUMBER REFERENCES DICH_VU(MADICHVU),
    GHICHU NVARCHAR2(255),
    PRIMARY KEY(MALICHHEN, MADICHVU),
    FOREIGN KEY(MALICHHEN, MAKH) REFERENCES LICH_HEN(MALICHHEN, MAKH) ON DELETE CASCADE
);

